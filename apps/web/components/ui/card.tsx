import * as React from "react";
import { cn } from "@/lib/utils";

export const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { surface?: 1 | 2 }
>(({ className, surface = 1, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded border",
      surface === 1 ? "bg-surface-1 border-border-subtle" : "bg-surface-2 border-border-default",
      className,
    )}
    {...props}
  />
));
Card.displayName = "Card";

